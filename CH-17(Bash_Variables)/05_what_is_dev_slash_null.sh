In case you don't wanna print the output of a command on terminal or write in a file 
then 
  we can redirect the output to 
       /dev/null

example -> 
      # cd /root &> /dev/null


&: This indicates that both stdout and stderr should be redirected
>: This is the usual output redirection operator, but used in combination with & here, it captures both output streams.

